void play(vector<int> v) {
  int rm=1;
  while(v.size()>2) {
    bool found=0;
    for (auto it=v.begin();it!=v.end();++it) {
      if (*it ==rm) {
        found=1;v.erase(it); break;
      }
    }
    if (!found) v.erase(v.begin());
    rm^=1;
  }
  final.insert(v);
}

