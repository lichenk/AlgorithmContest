#define BASET 25LL
#define RMAXT (MAXT + BASET)
#define START 0LL
#define END 1LL
#define CLASSNUM 2LL
ll INF_FLOW = -1;
typedef struct {
	ll t, rev, cap, f;
} EdgeStruct;
typedef EdgeStruct *Edge;
typedef vector<Edge> *Graph;
Graph graph;
ll *ptr, *dist;
ll global_num_nodes = -1;
ll global_num_plants = -1;

ll getvtx(ll plant, ll t, ll cls) {
	assert(BASET + t >= 0 && (BASET + t) < RMAXT);
	return (plant*CLASSNUM + cls)*RMAXT + (BASET + t);
}

Edge newEdge(ll t, ll rev, ll cap) {
	Edge edge = new EdgeStruct;
	edge->t = t;
	edge->rev = rev;
	edge->cap = cap;
	edge->f = 0;
	return edge;
}

void addEdge(ll s, ll t, ll cap) {
	graph[s].PB(newEdge(t, graph[t].size(), cap));
	graph[t].PB(newEdge(s, graph[s].size() - 1, 0));
}
void resetFlow() {
	ll n = global_num_nodes;
	for (ll i = 0; i < n; i++) {
		for (auto& e: graph[i]) {
			e->f = 0;
		}
	}
}
// Level graph
bool dinicBfs(ll src, ll dest) {
	////printf("Enter BFS\n");
	ll n = global_num_nodes;
	for (ll i = 0; i < n; i++) {dist[i] = -1;}
	queue<ll> q;
	q.push(src);
	dist[src] = 0;
	while(!q.empty()) {
		ll u = q.front();
		////printf("BFS %lld with dist %lld. %lld\n",u,dist[u], q.size());
		q.pop();
		////printf("S %lld\n",q.size());
		for (auto& e: graph[u]) {
			if (dist[e->t] < 0 && e->f < e->cap) {
				dist[e->t] = dist[u] + 1;
				q.push(e->t);
				////printf("BFS push %lld\n", e->t);
			}
		}
		////printf("E %lld\n",q.size());
		////printf("X %d\n",q.empty());
		if (q.empty()) break;
	}
	return dist[dest] >= 0;
}

ll dinicDfs(ll dest, ll u, ll f) {
	if (u == dest) {return f;}
	ll usz = graph[u].size();
	for (; ptr[u] < usz; ++ptr[u]) {
		Edge e = graph[u][ptr[u]];
		if (dist[e->t] == dist[u] + 1 && e->f < e->cap) {
			ll df = dinicDfs(dest, e->t, min(f, e->cap - e->f));
			if (df > 0) {
				/*printf("Flow %lld from", df);
				printvtx(u); printvtx(e->t);
				printf("\n");*/
				e->f += df;
				graph[e->t][e->rev]->f -= df;
				return df;
			}
		}
	}
	return 0;
}

ll maxFlow(ll src, ll dest, ll upper_limit) {
	ll flow = 0;
	ll n = global_num_nodes;
	while (dinicBfs(src, dest)) {
		for (ll i = 0; i < n; i++) {ptr[i] = 0;}
		while(1) {
			// CHANGE INF_FLOW TO LL_MAX
			ll df = dinicDfs(dest, src, INF_FLOW);
			// !!!
			if (df == 0) {break;}
			flow += df;
			// !!!! Awkward overflow condition !!!!
			if (flow > upper_limit) return flow;
			// !!! Remove if used in other code !!!!
		}
	}
	return flow;
}

class Farmville {
	public:
	int minTime(vector <string> s, vector <int> tim, vector <int> cost, int budget) {
		INF_FLOW = budget+1;
		ll numplants = s.size();
		global_num_plants = numplants;
		ll numnodes = numplants*CLASSNUM*RMAXT;
		ll target = numnodes; numnodes++;
		ll src[12];
		for (ll srcidx = 0; srcidx < 12; srcidx++) {
			src[srcidx] = numnodes; numnodes++;
		}
		graph = new vector<Edge>[numnodes];
		global_num_nodes = numnodes;
		dist = new ll[numnodes];
		ptr = new ll[numnodes];
		// Make graph
		for (ll plant = 0; plant < numplants; plant++) {
			for (ll t = -tim[plant]; t < 0; t++) {
				addEdge(getvtx(plant, t, END), target, cost[plant]);
			}
			for (ll t = 0; t < MAXT; t++) {
				addEdge(getvtx(plant, t, END), getvtx(plant, t, START), cost[plant]);
			}
			for (ll depend = 0; depend < numplants; depend++) {
				if (s[plant][depend] == '1') {
					ll flow = cost[depend];
					for (ll t = 0; t < MAXT; t++) {
						for (ll dt = 1; dt <= tim[depend]; dt++) {
							ll start_time = t - dt;
							addEdge(getvtx(plant, t, START), getvtx(depend, start_time, END), flow);
						}
						addEdge(getvtx(plant, t, START), getvtx(depend, t, START), INF_FLOW);
					}
				}
			}
		}
		ll imin = 0; ll imax = MAXT;
		ll srcidx = 0;
		while (imin < imax) {
			ll imid = (imin+imax)/2;

			ll t = imid;
			for (ll plant = 0; plant < numplants; plant++) {
				for (ll dt = 1; dt <= tim[plant]; dt++) {
					ll start_time = t - dt;
					addEdge(src[srcidx], getvtx(plant, start_time, END), cost[plant]);
				}
				addEdge(src[srcidx], getvtx(plant, t, END), INF_FLOW);
			}

			ll maxflow = maxFlow(src[srcidx], target, budget);
			//printf("%lld %lld\n", imid, maxflow);
			resetFlow();
			if (maxflow > budget) imin = imid + 1;
			else imax = imid;
			srcidx++;
		}
		assert(imax == imin);
		return imax;
	}
};
